package com.biz.classes;

/*
 * 클래스는 자바에서 기본적으로 제공하는
 * 변수를 선언하는 키워드(int, double, String)와
 * 사용법이 매우 유사하다.
 * 
 * 변수와 차이는
 * 변수는 자신이 저장할수 있는 type의 값만 저장할수 있지만
 * 클래스는 선언된 필드변수를 마음대로 조합하여
 * 		다양한 type의 값을 얼마든지 저장할 수 있다.
 * 
 * 선언된 클래스는 변수를 선언하는 것과 같이
 * Class 객체 = new ... 형식으로 선언하여 사용할 수 있다.
 * 
 */
public class Class_01 {

	/*
	 * 필드변수는 필요한 만큼 얼마든지 다양한 형식으로
	 * 선언할 수 있다.
	 */
	public String s1;
	public String s2;
	
	public int n1;
	public int n2;
	
	public double d1;
	public double d2;
	
	public boolean b1;
	public boolean b2;

}
