
public class Hello_03 {
	public static void main(String[] args) {
		
		System.out.println("30+40=" + (30+40)); // 덧셈
		System.out.println("30*40=" + (30*40)); // 곱셈
		System.out.println("40-30=" + (40-30)); // 뺄셈
		System.out.println("40/30=" + (40/30)); // 몫
		System.out.println("30%40=" + (30%40)); // 나머지

	}
}
