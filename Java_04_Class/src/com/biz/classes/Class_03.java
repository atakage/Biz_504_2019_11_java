package com.biz.classes;

public class Class_03 {

	public static void main(String[] args) {

		int num1 = 0;
		int num2 = 0;
		int sum = 0;
		
		num1 = 40; num2 = 50;
		
		sum = num1 + num2;
		System.out.printf("%d + %d = %d\n",num1,num2,sum);
		
		sum = num1 - num2;
		System.out.printf("%d - %d = %d\n",num1,num2,sum);

		sum = num1 * num2;
		System.out.printf("%d * %d = %d\n",num1,num2,sum);

		sum = num1 / num2;
		System.out.printf("%d / %d = %d\n",num1,num2,sum);

		sum = num1 % num2;
		System.out.printf("%d / %d의 나머지 = %d\n",num1,num2,sum);

		
		System.out.printf("%d 더하기 %d 는 %d 입니다",33,22,55);

		
	}

}
